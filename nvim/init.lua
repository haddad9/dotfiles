      vim.cmd.colorscheme 'gruber-darker'
    end,
  },

  { import = 'custom.plugins.compile-mode' },
  { import = 'custom.plugins.conform' },
  { import = 'custom.plugins.gitsigns' },
  { import = 'custom.plugins.mini' },
  { import = 'custom.plugins.multiple-cursors' },
